def update_file(fname, sections_by_result):
    path = os.path.join(TESTS_DIR, fname)
    with open(path, encoding="utf-8-sig") as f:
        data = json.load(f)
    for result_id, sections in sections_by_result.items():
        if result_id not in data["results"]:
            print(f"  WARNING: {result_id} not found in {fname}")
            continue
        existing = data["results"][result_id]["sections"]
        for i, (label, text) in enumerate(sections):
            if i < len(existing):
                existing[i]["text"] = text
            else:
                existing.append({"label": label, "text": text})
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    # regenerate _data.js
    stem = fname.replace(".json", "")
    js_path = os.path.join(TESTS_DIR, f"{stem}_data.js")
    with open(js_path, "w", encoding="utf-8") as f:
        f.write("window.QUIZ_DATA = ")
        f.write(json.dumps(data, ensure_ascii=False, indent=2))
        f.write(";")
    print(f"Updated {fname} + {stem}_data.js")


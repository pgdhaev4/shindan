def update_file(fname, custom_gauges):
    json_path = os.path.join(TESTS_DIR, f"{fname}.json")
    js_path   = os.path.join(TESTS_DIR, f"{fname}_data.js")
    labels    = SECTION_LABELS.get(fname, [])

    with open(json_path, "r", encoding="utf-8-sig") as f:
        data = json.load(f)

    data["customGauges"] = custom_gauges

    updated = 0
    for rid, result in data["results"].items():
        texts = SECTION_TEXTS.get(rid)
        if texts and labels:
            result["sections"] = [{"label": labels[i], "text": texts[i]} for i in range(min(len(labels), len(texts)))]
        if rid in SUMMARIES:
            result["summary"], result["description"] = SUMMARIES[rid]
        updated += 1

    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    with open(json_path, "r", encoding="utf-8") as f:
        content = f.read()
    js = f"// Auto-generated from {fname}.json\nwindow.QUIZ_DATA = {content};\n"
    with open(js_path, "w", encoding="utf-8") as f:
        f.write(js)

    print(f"[{fname}] {updated}件更新 -> data.js再生成")



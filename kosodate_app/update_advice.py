def update_advice(filename, advice_map):
    path = os.path.join(DATA_DIR, filename)
    with open(path, encoding="utf-8") as f:
        content = f.read()

    for name, new_text in advice_map.items():
        name_pattern = f'"name": "{re.escape(name)}"'
        match = re.search(name_pattern, content)
        if not match:
            print(f"  WARNING: '{name}' not found in {filename}")
            continue

        block_start = match.start()
        next_name = re.search(r'"name":', content[block_start + len(name_pattern):])
        block_end = (block_start + len(name_pattern) + next_name.start()) if next_name else len(content)
        block = content[block_start:block_end]

        # 既存のadvice値を新しいものに置換
        old_advice = re.search(r'"advice":\s*"([^"]*(?:\\.[^"]*)*)"', block)
        if not old_advice:
            print(f"  WARNING: advice not found for '{name}'")
            continue

        escaped_new = new_text.replace('\\', '\\\\').replace('"', '\\"')
        new_block = block[:old_advice.start()] + f'"advice": "{escaped_new}"' + block[old_advice.end():]
        content = content[:block_start] + new_block + content[block_end:]
        print(f"  OK: {name}")

    with open(path, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"Saved: {filename}")

for filename, advice_map in NEW_ADVICE.items():
    print(f"\n=== {filename} ===")
    update_advice(filename, advice_map)


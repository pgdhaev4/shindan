def add_advice(filename, advice_map):
    path = os.path.join(DATA_DIR, filename)
    with open(path, encoding="utf-8") as f:
        content = f.read()

    for name, advice in advice_map.items():
        # "name": "キャラ名" の直後にadviceを挿入（share_textの後）
        # share_textの行末に ",\n      "advice": "..." を追加
        # share_textはファイル内でキャラ名が含まれているshare_text行を特定
        # パターン: "share_text": "...名前を含む文字列..."  → 末尾に ,\n      "advice": "..." を追加
        # ただしすでにadviceが入っていたら追加しない

        # nameが一意に特定できるshare_textを探してadvice追加
        # 方法: "name": "キャラ名" から次の } までの中でshare_textを探す
        # より確実な方法: share_textの後ろが } か , かを見て判断

        # キャラ名でブロックを特定
        name_pattern = f'"name": "{re.escape(name)}"'
        match = re.search(name_pattern, content)
        if not match:
            print(f"  WARNING: '{name}' not found in {filename}")
            continue

        # そのキャラのブロック内のshare_textを探す
        block_start = match.start()
        # 次の "name": の位置（次のキャラ）か、results終了の } を探す
        next_name = re.search(r'"name":', content[block_start + len(name_pattern):])
        block_end = (block_start + len(name_pattern) + next_name.start()) if next_name else len(content)
        block = content[block_start:block_end]

        # すでにadviceが入っていたらスキップ
        if '"advice"' in block:
            print(f"  SKIP (already has advice): {name}")
            continue

        # share_textの末尾（"..."\nの直後）にadviceを追加
        share_pattern = r'("share_text":\s*"[^"]*(?:\\.[^"]*)*")'
        share_match = re.search(share_pattern, block)
        if not share_match:
            print(f"  WARNING: share_text not found for '{name}'")
            continue

        old_share = share_match.group(0)
        new_share = old_share + f',\n      "advice": "{advice}"'

        # contentの該当箇所だけ置換（最初の1回のみ）
        old_block = block
        new_block = block.replace(old_share, new_share, 1)
        content = content[:block_start] + new_block + content[block_end:]
        print(f"  OK: {name}")

    with open(path, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"Saved: {filename}")

for filename, advice_map in ADVICE.items():
    print(f"\n=== {filename} ===")
    add_advice(filename, advice_map)

print("\n完了！全48キャラにアドバイスを追加しました。")
